package com.testtask.applicationa.entity;


public class ImageModel {

    private int id;

    private String imageUrl;

    private int status;

    private String date;

    public ImageModel(String imageUrl, int status, String date) {
        this.imageUrl = imageUrl;
        this.status = status;
        this.date = date;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getImageUrl() {
        return imageUrl;
    }

    public void setImageUrl(String imageUrl) {
        this.imageUrl = imageUrl;
    }

    public int getStatus() {
        return status;
    }

    public void setStatus(int status) {
        this.status = status;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

}
